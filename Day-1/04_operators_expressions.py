# 1. Arithmetic Operators
a = 10
b = 3

print(f"a + b = {a + b}")  # Addition
print(f"a - b = {a - b}")  # Subtraction
print(f"a * b = {a * b}")  # Multiplication
print(f"a / b = {a / b}")  # Division
print(f"a % b = {a % b}")  # Modulus (remainder)
print(f"a ** b = {a ** b}") # Exponentiation (a to the power of b)

print("-" * 20)

# 2. Comparison Operators
x = 5
y = 10

print(f"x == y: {x == y}")  # Equal to
print(f"x != y: {x != y}")  # Not equal to
print(f"x > y: {x > y}")   # Greater than
print(f"x < y: {x < y}")   # Less than
print(f"x >= 5: {x >= 5}") # Greater than or equal to
print(f"y <= 10: {y <= 10}")# Less than or equal to

print("-" * 20)

# 3. Logical Operators
is_sunny = True
is_warm = False

print(f"Is it sunny AND warm? {is_sunny and is_warm}")
print(f"Is it sunny OR warm? {is_sunny or is_warm}")
print(f"Is it NOT sunny? {not is_sunny}")

print("-" * 20)

# 4. Membership Operators
fruits = ["apple", "banana", "cherry"]

print(f"Is 'banana' in fruits? {'banana' in fruits}")
print(f"Is 'orange' not in fruits? {'orange' not in fruits}") 